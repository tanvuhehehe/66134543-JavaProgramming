
public class TinhToanCoBan {

	public static void main(String[] args) {
		int a = 9;
		int b = 3;

		System.out.println("=== CÁC PHÉP TOÁN CƠ BẢN ===");

		System.out.printf("Cộng: %d + %d = %d\n", a, b, a + b);
		System.out.printf("Trừ: %d - %d = %d\n", a, b, a - b);
		System.out.printf("Nhân: %d * %d = %d\n", a, b, a * b);
		System.out.printf("Chia: %d / %d = %d\n", a, b, a / b);

	}

}
