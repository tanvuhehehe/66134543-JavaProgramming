package tvu;

public class Shape {
	private String color;
	private boolean filled;
	//constructor
	
	//getter và setter
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public boolean isFilled() {
		return filled;
	}
	public void setFilled(boolean filled) {
		this.filled = filled;
	}
	
	
}
