
public class congtrunhanchia {

	public static void main(String[] args) {

        int x = 20;
        int y = 10;

        System.out.println("Cộng: " + x + " + " + y + " = " + (x + y));
        System.out.println("Trừ: " + x + " - " + y + " = " + (x - y));
        System.out.println("Nhân: " + x + " * " + y + " = " + (x * y));
        System.out.printf("Chia: %d / %d = %.2f\n", x, y, (double)x / y);
        System.out.println("Phần dư: " + x + " % " + y + " = " + (x % y));
	}

}
